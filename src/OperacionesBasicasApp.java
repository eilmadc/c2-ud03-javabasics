public class OperacionesBasicasApp {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		/* Variables */
		int a = 13;
		int b = 42;
		
		/* Operaciones y salida en pantalla */ 
		System.out.println("Suma de a y b: "+(a+b));
		System.out.println("Resta de a y b: "+(a-b));
		System.out.println("Multiplicación de a y b: "+(a*b));
		System.out.println("División de a y b: "+(a/b));
		System.out.println("Resto de a y b: "+(a%b));
	}

}
